package LabWeek2;

public class SwitchCase {

	public static void main(String[] args) {
		int n = 2;
		switch(n)
		{
			case 1:
			{
				System.out.println("Hello");
				break;
			}
			case 2:
			{
					System.out.println("World");
					break;
			}
			default:
			{
				System.out.println("Error");
			}
		}
	}

}
